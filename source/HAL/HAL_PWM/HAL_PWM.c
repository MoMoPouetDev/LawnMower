void HAL_PWM_Init()
{
	LLD_PWM_Init(MOTOR_LEFT_FORWARD, MOTOR_LEFT_CHANNEL, 50000);
	LLD_PWM_Init(MOTOR_LEFT_BACKWARD, MOTOR_LEFT_CHANNEL, 50000);
	LLD_PWM_Init(MOTOR_RIGHT_FORWARD, MOTOR_RIGHT_CHANNEL, 50000);
	LLD_PWM_Init(MOTOR_RIGHT_BACKWARD, MOTOR_RIGHT_CHANNEL, 50000);
}


